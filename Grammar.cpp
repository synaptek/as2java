#include "Grammar.hpp"

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Grammar::Grammar(ParserState& _parserState)
:   Char("\x9\xA\xD\x20-\xFF")
,   Letter("A-Za-z")
,   Digit("0-9")
,   WhiteSpace("\x20\x9\xD\xA")
,   OpenCloseCurly("{}")
,   JunkChars("\x1A")
,   m_parserState(_parserState)
{
    NameChar = Letter | Digit | chset_t("_?");
}

