#include "Method.hpp"

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Method::Method(const std::string& _name, const std::string& _type, bool _static, bool _public)
:   m_name(_name)
,   m_type(_type)
,   m_static(_static)
,   m_public(_public)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Method::~Method()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Method::addParameter(const std::string& _parmType, const std::string& _parmName)
{
    m_parameters.push_back(std::make_pair<std::string, std::string>(_parmType, _parmName));
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Method::visitAllArgs(Method::I_ArgsVisitor& _visitor) const
{
    _visitor.begin();

    for(parm_list_type::const_iterator iter = m_parameters.begin(); iter != m_parameters.end(); iter++)
    {
        _visitor.visit(iter->first, iter->second);
    }

    _visitor.end();
}

